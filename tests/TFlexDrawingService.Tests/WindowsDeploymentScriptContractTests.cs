    [Fact]
    public void ServiceAccountAclLifecycleIsTransactional()
    {
        var installer = File.ReadAllText(InstallerPath);

        Assert.Contains("Get-DirectoryAclSnapshots", installer, StringComparison.Ordinal);
        Assert.Contains("Restore-DirectoryAclSnapshots", installer, StringComparison.Ordinal);
        Assert.Contains(
            "Remove-ExplicitDirectoryAccessForSid",
            installer,
            StringComparison.Ordinal);
        Assert.Contains("RemoveAccessRuleSpecific", installer, StringComparison.Ordinal);
        Assert.Contains(
            "$entry.FileSystemRights -ne $expectedRule.FileSystemRights",
            installer,
            StringComparison.Ordinal);
        Assert.Contains(
            "$entry.PropagationFlags -ne $expectedRule.PropagationFlags",
            installer,
            StringComparison.Ordinal);
        Assert.DoesNotContain("PurgeAccessRules", installer, StringComparison.Ordinal);

        var snapshot = installer.IndexOf(
            "Get-DirectoryAclSnapshots $managedAccessDirectories",
            StringComparison.Ordinal);
        var obsoleteAccessRemoval = installer.LastIndexOf(
            "Remove-ServiceFileSystemAccess `",
            StringComparison.Ordinal);
        var effectiveAcl = installer.IndexOf(
            "Write-Step \"Configuring effective service account file access\"",
            StringComparison.Ordinal);
        var deploymentFailure = installer.IndexOf(
            "Write-Warning \"Deployment validation failed: $deploymentErrorMessage Restoring the previous deployment.\"",
            StringComparison.Ordinal);
        var aclRestore = installer.IndexOf(
            "Restore-DirectoryAclSnapshots $directoryAclSnapshots",
            StringComparison.Ordinal);

        Assert.True(snapshot >= 0);
        Assert.True(obsoleteAccessRemoval > snapshot);
        Assert.True(effectiveAcl > obsoleteAccessRemoval);
        Assert.True(deploymentFailure > effectiveAcl);
        Assert.True(aclRestore > deploymentFailure);

        Assert.Contains("LsaEnumerateAccountRights", installer, StringComparison.Ordinal);
        Assert.Contains("LsaRemoveAccountRights", installer, StringComparison.Ordinal);
        Assert.Contains(
            "-GrantedByDeployment ([ref]$serviceLogonRightGrantedByDeployment)",
            installer,
            StringComparison.Ordinal);
        Assert.Contains(
            "Remove-LogOnAsServiceRightIfUnused",
            installer,
            StringComparison.Ordinal);
        Assert.Contains("Test-ServiceAccountSidInUse", installer, StringComparison.Ordinal);

        var rollbackPossible = installer.LastIndexOf(
            "Assert-ServiceAccountRollbackPossible",
            StringComparison.Ordinal);
        var rightGrant = installer.IndexOf(
            "Grant-LogOnAsServiceRight `",
            StringComparison.Ordinal);
        var rightRollback = installer.LastIndexOf(
            "Remove-LogOnAsServiceRightIfUnused `",
            StringComparison.Ordinal);

        Assert.True(rightGrant > rollbackPossible);
        Assert.True(rightRollback > deploymentFailure);
    }

