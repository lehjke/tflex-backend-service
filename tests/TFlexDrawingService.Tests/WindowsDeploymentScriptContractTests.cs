    [Fact]
    public void AutoGeneratedAdminCredentialIsRecoverableAcrossRollback()
    {
        var installer = File.ReadAllText(InstallerPath);
        var bootstrap = File.ReadAllText(BootstrapPath);

        var systemSecurityLoad = installer.IndexOf(
            "Add-Type -AssemblyName System.Security -ErrorAction Stop",
            StringComparison.Ordinal);
        var protectedDataUse = installer.IndexOf(
            "[Security.Cryptography.ProtectedData]",
            StringComparison.Ordinal);

        Assert.True(systemSecurityLoad >= 0 && systemSecurityLoad < protectedDataUse);
        Assert.Contains("ProtectedData]::Protect", installer, StringComparison.Ordinal);
        Assert.Contains("DataProtectionScope]::LocalMachine", installer, StringComparison.Ordinal);
        Assert.Contains("S-1-5-18", installer, StringComparison.Ordinal);
        Assert.Contains("S-1-5-32-544", installer, StringComparison.Ordinal);
        Assert.Contains("Read-BootstrapAdminRecovery", installer, StringComparison.Ordinal);
        Assert.Contains("Write-BootstrapAdminRecovery", installer, StringComparison.Ordinal);
        Assert.Contains("Remove-BootstrapAdminRecovery", installer, StringComparison.Ordinal);

        var recoveryWrite = installer.LastIndexOf(
            "Write-BootstrapAdminRecovery `",
            StringComparison.Ordinal);
        var serviceStart = installer.IndexOf(
            "Write-Step \"Starting Windows services\"",
            StringComparison.Ordinal);
        var passwordPrint = installer.LastIndexOf(
            "bootstrap admin password:",
            StringComparison.Ordinal);
        var recoveryRemove = installer.LastIndexOf(
            "Remove-BootstrapAdminRecovery $bootstrapAdminRecoveryPath",
            StringComparison.Ordinal);

        Assert.True(recoveryWrite >= 0 && recoveryWrite < serviceStart);
        Assert.True(passwordPrint >= 0 && passwordPrint < recoveryRemove);
        Assert.Contains(
            "& $FilePath @Arguments 2>&1 | ForEach-Object",
            bootstrap,
            StringComparison.Ordinal);
        Assert.DoesNotContain(
            "$output = & $FilePath @Arguments",
            bootstrap,
            StringComparison.Ordinal);
    }

